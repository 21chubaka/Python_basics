"""
Currency Exchange:
Program to take one currency as a float and converts it to another currency as a float
For this example will be using an amount in US Dollars to be converted into Canadian Dollars
"""

# US Dollar amount:
us_amt = 212005.03

# Canadian Dollar to US Dollar rate:
can_dol_rate = 1.26

print('Amount in US Dollars: ', us_amt)
print('Amount in Canadian Dollars: ', us_amt * can_dol_rate)